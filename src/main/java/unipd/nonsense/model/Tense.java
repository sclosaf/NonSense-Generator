package unipd.nonsense.model;

/**
 * Enumerates the grammatical tenses for verb conjugation and temporal reference.
 * <p>
 * This enum defines the three primary temporal classifications used in English
 * grammar, allowing for proper verb form selection and temporal relationship
 * expression in generated sentences.
 */
public enum Tense
{
	/**
	 * Represents actions or states that occurred in the past.
	 */
	PAST,

	/**
	 * Represents actions or states occurring in the present.
	 */
	PRESENT,

	/**
	 * Represents actions or states that will occur in the future.
	 */
	FUTURE

}
